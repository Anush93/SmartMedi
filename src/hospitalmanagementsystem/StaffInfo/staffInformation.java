package hospitalmanagementsystem.StaffInfo;

/**
 *
 * @author Anush
 */
public class staffInformation {
    private int employeeID;
    private String name;
    private String occupation;
    private String consultant;
    private String birthday;
    private String gender;
    private int teleNo;
    private String address;
    private int SearchEmployeeId;
    private int UpdateEmployeeId;

    /**
     * @return the employeeID
     */
    public int getEmployeeID() {
        return employeeID;
    }

    /**
     * @param employeeID the employeeID to set
     */
    public void setEmployeeID(int employeeID) {
        this.employeeID = employeeID;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the occupation
     */
    public String getOccupation() {
        return occupation;
    }

    /**
     * @param occupation the occupation to set
     */
    public void setOccupation(String occupation) {
        this.occupation = occupation;
    }

    /**
     * @return the consultant
     */
    public String getConsultant() {
        return consultant;
    }

    /**
     * @param consultant the consultant to set
     */
    public void setConsultant(String consultant) {
        this.consultant = consultant;
    }

    /**
     * @return the birthday
     */
    public String getBirthday() {
        return birthday;
    }

    /**
     * @param birthday the birthday to set
     */
    public void setBirthday(String birthday) {
        this.birthday = birthday;
    }

    /**
     * @return the gender
     */
    public String getGender() {
        return gender;
    }

    /**
     * @param gender the gender to set
     */
    public void setGender(String gender) {
        this.gender = gender;
    }

    /**
     * @return the teleNo
     */
    public int getTeleNo() {
        return teleNo;
    }

    /**
     * @param teleNo the teleNo to set
     */
    public void setTeleNo(int teleNo) {
        this.teleNo = teleNo;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the SearchEmployeeId
     */
    public int getSearchEmployeeId() {
        return SearchEmployeeId;
    }

    /**
     * @param SearchEmployeeId the SearchEmployeeId to set
     */
    public void setSearchEmployeeId(int SearchEmployeeId) {
        this.SearchEmployeeId = SearchEmployeeId;
    }

    /**
     * @return the UpdateEmployeeId
     */
    public int getUpdateEmployeeId() {
        return UpdateEmployeeId;
    }

    /**
     * @param UpdateEmployeeId the UpdateEmployeeId to set
     */
    public void setUpdateEmployeeId(int UpdateEmployeeId) {
        this.UpdateEmployeeId = UpdateEmployeeId;
    }
    
    
}
