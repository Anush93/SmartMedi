    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed
          staffInformation si = new staffInformation();
         
        si.setName(txtName.getText());
        si.setOccupation(cbOccupation.getSelectedItem().toString());
        si.setConsultant(cbConsaltant.getSelectedItem().toString());
        si.setGender(cbGender.getSelectedItem().toString());
        si.setBirthday(txtBirthday.getText());
        si.setAddress(txtAddress.getText());
        si.setUpdateEmployeeId(new Integer(txtUpdateEmployeeId.getText()));
        si.setTeleNo(new Integer(txtTeleNo.getText()));
        
        
        if(dbS.updatestaffInfo(si)){
            JOptionPane.showMessageDialog(this, "Update successfully");
            clearFields();
            
        }else{
            JOptionPane.showMessageDialog(this, "Error while updating");
        }
    }//GEN-LAST:event_btnSaveChangesActionPerformed

