    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       staffInformation si = new staffInformation();
        
        try {
            
        
        si.setEmployeeID(0);
        si.setName(txtName.getText());
        si.setOccupation(cbOccupation.getSelectedItem().toString());
        si.setConsultant(cbConsaltant.getSelectedItem().toString());
        si.setBirthday(txtBirthday.getText());
        si.setGender(cbGender.getSelectedItem().toString());
        si.setTeleNo(Integer.parseInt(txtTeleNo.getText()));
        si.setAddress(txtAddress.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        try {
            dbS.staffInfo(si);
            JOptionPane.showMessageDialog(this, "successfully Inserted!! ");
            clearFields();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Please check your input details");
            
            JOptionPane.showMessageDialog(this, ex);
        }
       
    }//GEN-LAST:event_btnSaveActionPerformed

