    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       staffAttend sa = new staffAttend();
        
        try {
            
        
        sa.setAttendID(0);
        sa.seteID(Integer.parseInt(txtEID.getText()));
        sa.setDate(txtDate.getText());
        sa.setIntime(txtIntime.getText());
        sa.setOutTime(txtOuttime.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        try {
            dbSA.staffAtt(sa);
            JOptionPane.showMessageDialog(this, "successfully Inserted!! ");
            clearFields();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Please check your input details");
            
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

