    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed
          staffAttend sa = new staffAttend();
         
        sa.setDate(txtDate.getText());
        sa.setIntime(txtIntime.getText());
        sa.setOutTime(txtOuttime.getText());
        sa.setUpdateAttendanceId(new Integer(txtUpdateAttendanceId.getText()));
        sa.seteID(Integer.parseInt(txtEID.getText()));
       
        
        if(dbSA.updatestaffAttend(sa)){
            JOptionPane.showMessageDialog(this, "Update successfully");
            clearFields();
            
        }else{
            JOptionPane.showMessageDialog(this, "Error while updating");
        }
    }//GEN-LAST:event_btnSaveChangesActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         staffAttend sa = new staffAttend();
         sa.setUpdateAttendanceId(new Integer(txtUpdateAttendanceId.getText()));
        if(dbSA.deleteStaffAttendance(sa)){
            JOptionPane.showMessageDialog(this, "Delete successfully");
            clearFields();
            
        }else{
            JOptionPane.showMessageDialog(this, "Error while deleting");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


