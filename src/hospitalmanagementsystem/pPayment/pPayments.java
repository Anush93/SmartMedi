    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed
        patientPayments pp = new patientPayments();
        
        pp.setTesting(new Integer(txtTesting.getText()));
        pp.setTreatMedcine(new Integer(txtTreatMeDCharges.getText()));
        pp.setWardCharges(new Integer(txtWardCost.getText()));
        pp.setpRegNo(new Integer(txtRegNo.getText()));
        pp.setUpdatePaymentId(new Integer(txtPaymentId.getText()));
        
        
        if(dbp.updatePPayments(pp)){
            JOptionPane.showMessageDialog(this, "Update successfully");
            clearFields();
            
        }else{
            JOptionPane.showMessageDialog(this, "Error while updating");
        }
    }//GEN-LAST:event_btnSaveChangesActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         patientPayments pp = new patientPayments();
        pp.setUpdatePaymentId(new Integer(txtPaymentId.getText()));
        if(dbp.deletePatientPayment(pp)){
            JOptionPane.showMessageDialog(this, "Delete successfully");
            clearFields();
            
        }else{
            JOptionPane.showMessageDialog(this, "Error while deleting");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


