    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
         patientPayments pp = new patientPayments();
        
        try {
            
        
        pp.setpPaymentId(0);
        pp.setWardCharges(Integer.parseInt(txtWardCost.getText()));
        pp.setTreatMedcine(Integer.parseInt(txtTreatMeDCharges.getText()));
        pp.setTesting(Integer.parseInt(txtTesting.getText()));
        pp.setpRegNo(Integer.parseInt(txtRegNo.getText()));
        
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        try {
            dbp.patientPay(pp);
            JOptionPane.showMessageDialog(this, "successfully Inserted!! ");
            clearFields();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Please check your input details");
            
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
      
    }//GEN-LAST:event_btnSaveActionPerformed

