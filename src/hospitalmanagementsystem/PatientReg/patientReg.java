    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
       patientRegistration pr = new patientRegistration();
        
        try {
            
        
        pr.setpRegId(0);
        pr.setDate(txtDate.getText());
        pr.setExPhysician(txtExPhysician.getText());
        pr.setComfirmedConsultantId(Integer.parseInt(txtComConsultant.getText()));
        pr.setWardNo(Integer.parseInt(cbWard.getSelectedItem().toString()));
        pr.setpId(Integer.parseInt(txtPid.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        try {
            dbPReg.patientRegistr(pr);
            MaxRegNO();
           
            clearFields();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Please check your input details");
            
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
      
       
      
       
    }//GEN-LAST:event_btnSaveActionPerformed

