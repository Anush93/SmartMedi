    private void btnSaveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveChangesActionPerformed
         patientRegistration pr = new patientRegistration();
         
        pr.setDate(txtDate.getText());
        pr.setExPhysician(txtExPhysician.getText());
        pr.setpId(new Integer(txtPid.getText()));
        pr.setComfirmedConsultantId(new Integer(txtComConsultant.getText()));
        pr.setWardNo(new Integer(cbWard.getSelectedItem().toString()));
        pr.setUpdateRegID(new Integer(txtUpdateRegId.getText()));
        
        
        if(dbPReg.updatePatientReg(pr)){
            JOptionPane.showMessageDialog(this, "Update successfully");
            clearFields();
            
        }else{
            JOptionPane.showMessageDialog(this, "Error while updating");
        }
    }//GEN-LAST:event_btnSaveChangesActionPerformed

